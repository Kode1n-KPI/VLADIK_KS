`timescale 1ns / 1ps

module decoder4_16_tb;

    // ?????? ??????? (reg)
    reg [3:0] t_in;
    reg t_enable;

    // ??????? ??????? (wire)
    wire [15:0] t_out;

    // ????????? ?????????? ???????????
    decoder4_16 uut (
        .in(t_in),
        .enable(t_enable),
        .out(t_out)
    );

    initial begin
        // ?????? ?????? ? ???????
        $display("Time\tE\tIn\tOutput (Hex)");
        $monitor("%0t\t%b\t%d\t%h", $time, t_enable, t_in, t_out);

        // ???????? ???????? ??????????
        t_enable = 0;
        t_in = 4'b0000;
        #20;

        // ???????? ??????????
        t_enable = 1;

        // ???? ??? ???????? ???? 16 ??????????
        repeat (16) begin
            #20 t_in = t_in + 1;
        end

        // ???? ?????????
        #20 $stop;
    end

endmodule